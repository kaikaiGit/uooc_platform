async function initCourseWaveData() {
    if (!db) {
        console.error('数据库未初始化');
        return;
    }

    try {
        const transaction = db.transaction(['coursewares'], 'readwrite');
        const store = transaction.objectStore('coursewares');
        
        const countRequest = store.count();
        
        countRequest.onsuccess = function() {
            if (countRequest.result === 0) {
                coursewave.forEach(note => {
                    store.add(note);
                });
                console.log('课件数据初始化完成，共添加', coursewave.length, '条记录');
            } else {
                console.log('课件数据已存在（现有', countRequest.result, '条），跳过初始化');
            }
        };
        
        countRequest.onerror = function(event) {
            console.error('统计课件数据失败:', event.target.error);
        };
    } catch (error) {
        console.error('初始化课件数据失败:', error);
    }
}

// 暴露到全局
window.initCourseWaveData = initCourseWaveData;